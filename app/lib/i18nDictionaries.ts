export function getBuiltInTranslation(
  language: LanguageCode,
  namespace: string,
  key: string
) {
  if (language === "en") return null;
  return BUILTIN_TRANSLATION_DICTIONARIES[language]?.[flatKey(namespace, key)] ?? null;
}

export function getBuiltInDictionaryEntries(language: LanguageCode) {
  const dictionary = BUILTIN_TRANSLATION_DICTIONARIES[language] ?? {};

  return Object.entries(dictionary).map(([compoundKey, translatedText]) => {
    const separatorIndex = compoundKey.indexOf(":");
    return {
      namespace: compoundKey.slice(0, separatorIndex),
      key: compoundKey.slice(separatorIndex + 1),
      baseText: translatedText,
      translatedText,
    };
  });
}
